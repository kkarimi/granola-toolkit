#!/usr/bin/env node

import { mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import { join, resolve } from "node:path";

import { build } from "vite-plus";
import solid from "vite-plugin-solid";

const root = resolve(import.meta.dirname, "..");
const outputFile = resolve(root, "src/web/generated.ts");
const checkMode = process.argv.includes("--check");

async function buildAssets() {
  const tempDir = await mkdtemp(join(tmpdir(), "granola-web-build-"));

  try {
    await build({
      build: {
        cssCodeSplit: false,
        emptyOutDir: true,
        lib: {
          entry: resolve(root, "src/web-app/main.tsx"),
          fileName: () => "granola-web-client.js",
          formats: ["es"],
        },
        minify: false,
        outDir: tempDir,
        rollupOptions: {
          output: {
            assetFileNames: "granola-web-client.[ext]",
          },
        },
        target: "es2020",
      },
      configFile: false,
      plugins: [solid()],
      root,
    });

    const js = await readFile(join(tempDir, "granola-web-client.js"), "utf8");
    const css = await readFile(join(tempDir, "granola-web-client.css"), "utf8");
    return { css, js };
  } finally {
    await rm(tempDir, { force: true, recursive: true });
  }
}

function generatedModule(assets) {
  const asTemplateLiteral = (value) =>
    `String.raw\`${value.replaceAll("`", "\\`").replaceAll("${", "\\${")}\``;
  return `// Generated by \`npm run web:build\`. Do not edit by hand.\nexport const granolaWebClientCss = ${asTemplateLiteral(
    assets.css,
  )};\nexport const granolaWebClientJs = ${asTemplateLiteral(assets.js)};\n`;
}

async function main() {
  const assets = await buildAssets();
  const next = generatedModule(assets);

  if (checkMode) {
    let current = "";
    try {
      current = await readFile(outputFile, "utf8");
    } catch {}

    if (current !== next) {
      console.error("Web client bundle is out of date. Run `npm run web:build`.");
      process.exitCode = 1;
    }
    return;
  }

  await writeFile(outputFile, next, "utf8");
}

try {
  await main();
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
}
